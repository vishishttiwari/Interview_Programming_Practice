package util;

import java.util.ArrayList;

class WeightedEdge {
	public Node from;	
	public Node to;
	public double weight;
	
	public WeightedEdge(Node from, Node to, double weight) {
		this.from = from;
		this.to = to;
		this.weight = weight;
	}
}

public class Weighted_Node {
	public int value;
	public ArrayList<WeightedEdge> list;
	
	public Weighted_Node(int value) {
		this.value = value;
		list = new ArrayList<WeightedEdge>();
	}
}
